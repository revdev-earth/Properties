"use client";

import React, { useState, useEffect } from "react";

import { Equipment as EquipmentType, Maintenance } from "@prisma/client";

import { PropsJustParams } from "../types";
import { getEquipments } from " +/actions/property/actions_and_mutations";

type EquipmentWithMaintenance = EquipmentType & {
  maintenances: Maintenance[];
};

const Equipments = ({ params }: PropsJustParams) => {
  const [equipments, setEquipments] = useState<EquipmentWithMaintenance[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    const fetchEquipmentData = async () => {
      try {
        setLoading(true);
        const resolvedParams = await params;
        const id = resolvedParams.id;
        if (id) {
          const fetchedEquipments = await getEquipments({ id });
          if (fetchedEquipments) {
            setEquipments(fetchedEquipments);
          } else {
            throw new Error("No equipment data found.");
          }
        }
      } catch (error) {
        console.trace({ error });
        setError("Failed to fetch equipment data.");
      } finally {
        setLoading(false);
      }
    };

    fetchEquipmentData();
  }, [params]);

  if (loading) {
    return (
      <div className="p-6 text-center text-gray-500">
        Loading equipment data...
      </div>
    );
  }

  if (error) {
    return <div className="p-6 text-center text-red-500">Error: {error}</div>;
  }

  if (!equipments || equipments.length === 0) {
    return (
      <div className="p-6 text-center text-gray-500">
        No equipment data available.
      </div>
    );
  }

  return (
    <div className="p-6 bg-gray-100 min-h-screen">
      <h1 className="text-3xl font-bold text-center mb-8">Equipments</h1>

      {equipments.map((equipment) => {
        const {
          id,
          name,
          maintenanceFrequency,
          lastMaintenanceDate,
          nextMaintenanceDate,
          warrantyPolicy,
          maintenanceCost,
          mapCoordinates,
          maintenances,
        } = equipment;

        return (
          <div key={id} className="bg-white shadow rounded-lg p-6 mb-6">
            {/* Equipment Overview */}
            <h2 className="text-2xl font-semibold mb-4">{name}</h2>
            <p className="text-lg">
              <span className="font-medium">Maintenance Frequency:</span>{" "}
              {maintenanceFrequency}
            </p>
            <p className="text-lg">
              <span className="font-medium">Last Maintenance Date:</span>{" "}
              {new Date(lastMaintenanceDate).toLocaleDateString()}
            </p>
            <p className="text-lg">
              <span className="font-medium">Next Maintenance Date:</span>{" "}
              {nextMaintenanceDate
                ? new Date(nextMaintenanceDate).toLocaleDateString()
                : "Not scheduled"}
            </p>
            <p className="text-lg">
              <span className="font-medium">Warranty Policy:</span>{" "}
              {warrantyPolicy || "Not specified"}
            </p>
            <p className="text-lg">
              <span className="font-medium">Maintenance Cost:</span>{" "}
              {maintenanceCost
                ? `$${maintenanceCost.toFixed(2)}`
                : "Not specified"}
            </p>
            {mapCoordinates && (
              <p className="text-lg">
                <span className="font-medium">Map Coordinates:</span>{" "}
                {mapCoordinates}
              </p>
            )}

            {/* Maintenance History */}
            <div className="mt-6">
              <h3 className="text-xl font-semibold mb-4">
                Maintenance History
              </h3>
              {maintenances && maintenances.length > 0 ? (
                <ul className="space-y-3">
                  {maintenances.map((maintenance, index) => (
                    <li
                      key={index}
                      className="p-4 bg-gray-50 rounded-md shadow"
                    >
                      <p className="text-lg font-medium">
                        Description: {maintenance.description}
                      </p>
                      <p className="text-sm text-gray-600">
                        Category: {maintenance.category}
                      </p>
                      <p className="text-sm text-gray-600">
                        Type: {maintenance.type}
                      </p>
                      <p className="text-sm text-gray-600">
                        Maintenance Date:{" "}
                        {new Date(
                          maintenance.maintenanceDate
                        ).toLocaleDateString()}
                      </p>
                      {maintenance.nextMaintenanceDate && (
                        <p className="text-sm text-gray-600">
                          Next Maintenance Date:{" "}
                          {new Date(
                            maintenance.nextMaintenanceDate
                          ).toLocaleDateString()}
                        </p>
                      )}
                      <p className="text-sm text-gray-600">
                        Cost Responsible: {maintenance.costResponsible}
                      </p>
                      <p className="text-sm text-gray-600">
                        Repair Cost:{" "}
                        {maintenance.repairCost
                          ? `$${maintenance.repairCost.toFixed(2)}`
                          : "N/A"}
                      </p>
                      {maintenance.warranty && (
                        <p className="text-sm text-gray-600">
                          Warranty: {maintenance.warranty}
                        </p>
                      )}
                      <p className="text-sm text-gray-600">
                        Status: {maintenance.status || "Not specified"}
                      </p>
                      {maintenance.observations && (
                        <p className="text-sm text-gray-600">
                          Observations: {maintenance.observations}
                        </p>
                      )}
                    </li>
                  ))}
                </ul>
              ) : (
                <p className="text-gray-500">
                  No maintenance history available.
                </p>
              )}
            </div>
          </div>
        );
      })}
    </div>
  );
};

export default Equipments;
